package formatter

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/multigent/multigent/internal/ctxbuild"
)

// claudeCodeFormatter generates the file layout for Claude Code.
//
// Output structure inside outDir:
//
//	CLAUDE.md                    ← main file; uses @import to reference layers
//	.multigent/
//	  context/
//	    agency.md
//	    role-<team>-<role>.md
//	    project-<project>.md
//	    wakeup.md              ← playbook (installed by project apply / sync)
//	  agent.yaml               ← agent metadata
//	  tasks.yaml              ← active task queue
//	  tasks_archive.yaml      ← archived tasks
//	  heartbeat.yaml          ← wakeup config
//	  crons.yaml              ← scheduled tasks
//	  messages.yaml           ← async messages
//	  runs/                   ← execution logs
//	  tasks/                  ← role workspace (from role setup)
//	  roadmap/                ← role workspace (from role setup)
//	  okr/                    ← role workspace (from role setup)
//	.claude/
//	  skills/
//	    git/
//	      SKILL.md
//	    bash/
//	      SKILL.md
//
// By keeping each layer as a separate file and referencing them via @import,
// we stay well under Claude Code's recommended 200-line limit per CLAUDE.md,
// and `multigent sync` can patch only the files whose content changed.
type claudeCodeFormatter struct{}

func (f *claudeCodeFormatter) Format(mc *ctxbuild.MergedContext, outDir string) error {
	contextDir := filepath.Join(outDir, ".multigent", "context")
	if err := os.MkdirAll(contextDir, 0o755); err != nil {
		return fmt.Errorf("claudecode: create context dir: %w", err)
	}

	var importLines []string

	// Write each layer into its own file under .multigent/context/
	for _, layer := range mc.Layers {
		filename := sourceToFilename(layer.Source) + ".md"
		path := filepath.Join(contextDir, filename)
		if err := os.WriteFile(path, []byte(layer.Content), 0o644); err != nil {
			return fmt.Errorf("claudecode: write layer %q: %w", layer.Source, err)
		}
		// CLAUDE.md @import paths are relative to the file containing the import.
		importLines = append(importLines, fmt.Sprintf("@.multigent/context/%s", filename))
	}

	// If a playbook (wakeup routine) has been installed, include it so the agent
	// loads the full SOP at session start rather than receiving it each wakeup.
	wakeupMD := filepath.Join(contextDir, "wakeup.md")
	if _, err := os.Stat(wakeupMD); err == nil {
		importLines = append(importLines, "@.multigent/context/wakeup.md")
	}

	// Write CLAUDE.md with @import directives
	claudeMD := buildClaudeMD(importLines)
	if err := os.WriteFile(filepath.Join(outDir, "CLAUDE.md"), []byte(claudeMD), 0o644); err != nil {
		return fmt.Errorf("claudecode: write CLAUDE.md: %w", err)
	}

	// Write skills as .claude/skills/<name>/SKILL.md
	if err := f.writeSkills(mc.Skills, outDir); err != nil {
		return err
	}

	return nil
}

func (f *claudeCodeFormatter) writeSkills(skills []ctxbuild.SkillDef, outDir string) error {
	for _, sk := range skills {
		skillDir := filepath.Join(outDir, ".claude", "skills", sk.Name)
		// Remove stale bundled files (e.g. old bin/ copies) before redeploy.
		if err := os.RemoveAll(skillDir); err != nil && !os.IsNotExist(err) {
			return fmt.Errorf("claudecode: reset skill dir %q: %w", sk.Name, err)
		}
		if err := os.MkdirAll(skillDir, 0o755); err != nil {
			return fmt.Errorf("claudecode: create skill dir %q: %w", sk.Name, err)
		}
		// Copy any bundled files (scripts etc.) and resolve {{SKILL_DIR}}.
		absSkillDir, err := deploySkillFiles(sk, skillDir)
		if err != nil {
			return fmt.Errorf("claudecode: %w", err)
		}
		resolved := sk
		resolved.Prompt = resolveSkillDir(sk.Prompt, absSkillDir)
		skillMD := buildSkillMD(resolved)
		path := filepath.Join(skillDir, "SKILL.md")
		if err := os.WriteFile(path, []byte(skillMD), 0o644); err != nil {
			return fmt.Errorf("claudecode: write skill %q: %w", sk.Name, err)
		}
	}
	return nil
}

// buildClaudeMD renders the top-level CLAUDE.md content.
func buildClaudeMD(importLines []string) string {
	var sb strings.Builder
	sb.WriteString("<!-- Generated by multigent. Do not edit manually. Run `multigent sync` to update. -->\n\n")
	for _, line := range importLines {
		sb.WriteString(line)
		sb.WriteString("\n")
	}
	return sb.String()
}

// buildSkillMD renders a SKILL.md file for Claude Code's skill system.
func buildSkillMD(sk ctxbuild.SkillDef) string {
	var sb strings.Builder
	sb.WriteString("---\n")
	sb.WriteString(fmt.Sprintf("name: %s\n", sk.Name))
	if sk.Description != "" {
		sb.WriteString(fmt.Sprintf("description: %s\n", sk.Description))
	}
	sb.WriteString("---\n\n")
	if sk.Prompt != "" {
		sb.WriteString(sk.Prompt)
		if !strings.HasSuffix(sk.Prompt, "\n") {
			sb.WriteString("\n")
		}
	}
	return sb.String()
}

// sourceToFilename converts a layer source key to a safe filename prefix.
// e.g. "role:<team>/<role>" → "role-team-role"
func sourceToFilename(source string) string {
	s := strings.ReplaceAll(source, ":", "-")
	s = strings.ReplaceAll(s, "/", "-")
	return s
}
